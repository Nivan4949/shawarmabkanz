/* ==========================================================================
   CUSTOMIZER MODAL HANDLERS
   ========================================================================== */
function initCustomizerModal() {
  const modal = document.getElementById("custom-modal");
  const closeBtn = document.getElementById("modal-close-btn");
  const spiceButtons = document.querySelectorAll(".spice-options .option-btn");
  const addBtn = document.getElementById("modal-add-to-cart-btn");
  
  closeBtn.addEventListener("click", () => {
    modal.classList.remove("active");
  });
  
  // Close on outside click
  modal.addEventListener("click", (e) => {
    if (e.target === modal) {
      modal.classList.remove("active");
    }
  });

  spiceButtons.forEach(btn => {
    btn.addEventListener("click", () => {
      spiceButtons.forEach(b => b.classList.remove("active"));
      btn.classList.add("active");
      modalSelectedSpice = btn.getAttribute("data-spice");
    });
  });

  addBtn.addEventListener("click", () => {
    if (!modalSelectedItem) return;

    // Grab customizations
    const size = modalSelectedItem.sizes[modalSelectedSizeIndex];
    const sizeName = currentLang === "en" ? size.labelEn : size.labelAr;
    const spiceName = currentLang === "en" 
      ? translationsEn[`spice_${modalSelectedSpice}`] 
      : translationsAr[`spice_${modalSelectedSpice}`];
      
    const extraPrice = size.priceModifier + modalSelectedAddons.reduce((sum, addon) => sum + addon.price, 0);
    const finalUnitPrice = modalSelectedItem.price + extraPrice;
    
    const cartItem = {
      cartId: `${modalSelectedItem.id}_${modalSelectedSizeIndex}_${modalSelectedSpice}_${modalSelectedAddons.map(a => a.id).join('-')}`,
      id: modalSelectedItem.id,
      nameEn: modalSelectedItem.nameEn,
      nameAr: modalSelectedItem.nameAr,
      sizeNameEn: size.labelEn,
      sizeNameAr: size.labelAr,
      spiceNameEn: translationsEn[`spice_${modalSelectedSpice}`],
      spiceNameAr: translationsAr[`spice_${modalSelectedSpice}`],
      addons: modalSelectedAddons,
      unitPrice: finalUnitPrice,
      quantity: 1,
      image: "assets/hero_shawarma.png"
    };

    addToCart(cartItem);
    modal.classList.remove("active");
  });
}

window.openCustomizerModal = function(itemId) {
  const item = menuItems.find(i => i.id === itemId);
  if (!item) return;
  
  modalSelectedItem = item;
  modalSelectedSizeIndex = 0;
  modalSelectedSpice = "regular";
  modalSelectedAddons = [];
  
  const modal = document.getElementById("custom-modal");
  const title = document.getElementById("modal-item-title");
  const desc = document.getElementById("modal-item-desc");
  const img = document.getElementById("modal-item-image");
  
  title.textContent = currentLang === "en" ? item.nameEn : item.nameAr;
  desc.textContent = currentLang === "en" ? item.descEn : item.descAr;
  img.src = "assets/hero_shawarma.png";
  
  // Render Size Options
  const sizeContainer = document.querySelector(".size-options");
  sizeContainer.innerHTML = "";
  item.sizes.forEach((sz, idx) => {
    const label = currentLang === "en" ? sz.labelEn : sz.labelAr;
    const modifierText = sz.priceModifier > 0 ? ` (+$${sz.priceModifier.toFixed(2)})` : '';
    const btn = document.createElement("button");
    btn.className = `option-btn ${idx === 0 ? 'active' : ''}`;
    btn.textContent = `${label}${modifierText}`;
    btn.addEventListener("click", () => {
      sizeContainer.querySelectorAll(".option-btn").forEach(b => b.classList.remove("active"));
      btn.classList.add("active");
      modalSelectedSizeIndex = idx;
      updateModalPrice();
    });
    sizeContainer.appendChild(btn);
  });
  
  // Reset Spice Level UI
  const spiceButtons = document.querySelectorAll(".spice-options .option-btn");
  spiceButtons.forEach(btn => {
    btn.classList.remove("active");
    if (btn.getAttribute("data-spice") === "regular") {
      btn.classList.add("active");
    }
  });
  
  // Render Addons
  const addonContainer = document.querySelector(".addons-options");
  addonContainer.innerHTML = "";
  item.addons.forEach((ad, idx) => {
    const label = currentLang === "en" ? ad.labelEn : ad.labelAr;
    const btn = document.createElement("button");
    btn.className = "option-btn";
    btn.textContent = `${label} (+$${ad.price.toFixed(2)})`;
    btn.addEventListener("click", () => {
      btn.classList.toggle("active");
      
      const existsIdx = modalSelectedAddons.findIndex(a => a.labelEn === ad.labelEn);
      if (existsIdx > -1) {
        modalSelectedAddons.splice(existsIdx, 1);
      } else {
        modalSelectedAddons.push({
          id: idx,
          labelEn: ad.labelEn,
          labelAr: ad.labelAr,
          price: ad.price
        });
      }
      updateModalPrice();
    });
    addonContainer.appendChild(btn);
  });
  
  updateModalPrice();
  modal.classList.add("active");
};

function updateModalPrice() {
  if (!modalSelectedItem) return;
  
  const size = modalSelectedItem.sizes[modalSelectedSizeIndex];
  const sizePrice = size ? size.priceModifier : 0;
  const addonsPrice = modalSelectedAddons.reduce((sum, ad) => sum + ad.price, 0);
  const total = modalSelectedItem.price + sizePrice + addonsPrice;
  
  document.getElementById("modal-total-price-val").textContent = `$${total.toFixed(2)}`;
}

