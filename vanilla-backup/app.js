function initCartDrawer() {
  const drawer = document.getElementById("cart-drawer");
  const openBtn = document.getElementById("cart-toggle-btn");
  const closeBtn = document.getElementById("cart-close-btn");
  const startOrderBtn = document.getElementById("start-ordering-btn");
  const checkoutBtn = document.getElementById("checkout-btn");
  
  const toggleDrawer = () => drawer.classList.toggle("active");
  
  openBtn.addEventListener("click", toggleDrawer);
  closeBtn.addEventListener("click", () => drawer.classList.remove("active"));
  drawer.addEventListener("click", (e) => {
    if (e.target === drawer) drawer.classList.remove("active");
  });
  
  startOrderBtn.addEventListener("click", () => drawer.classList.remove("active"));
  
  checkoutBtn.addEventListener("click", () => {
    if (cart.length === 0) return;
    drawer.classList.remove("active");
    
    // Open checkout success simulation
    openCheckoutSuccess();
    cart = [];
    renderCart();
  });
}

function addToCart(newItem) {
  // Check if item with exact specifications already exists
  const existingIdx = cart.findIndex(c => c.cartId === newItem.cartId);
  if (existingIdx > -1) {
    cart[existingIdx].quantity += 1;
  } else {
    cart.push(newItem);
  }
  
  renderCart();
  
  // Visual bounce effect on the navbar bag
  const badge = document.getElementById("cart-count-badge");
  badge.classList.remove("badge-bounce");
  void badge.offsetWidth; // Trigger reflow to restart animation
  badge.style.animation = "badgePop 0.4s ease-out";
}

function renderCart() {
  const list = document.getElementById("cart-items-container");
  const totalBadge = document.getElementById("cart-count-badge");
  const subtotalVal = document.getElementById("cart-subtotal-val");
  const taxVal = document.getElementById("cart-tax-val");
  const grandVal = document.getElementById("cart-total-val");
  const cartSummaryFooter = document.getElementById("cart-footer");
  
  const count = cart.reduce((sum, item) => sum + item.quantity, 0);
  totalBadge.textContent = count;
  document.getElementById("cart-drawer-count").textContent = `(${count})`;
  
  if (cart.length === 0) {
    list.innerHTML = `
      <div class="empty-cart-message">
        <span class="empty-cart-icon">🛒</span>
        <p>${currentLang === 'en' ? translationsEn.cart_empty : translationsAr.cart_empty}</p>
        <a href="#menu" id="start-ordering-btn" class="btn btn-outline" onclick="document.getElementById('cart-drawer').classList.remove('active')">
          ${currentLang === 'en' ? translationsEn.cart_start_order : translationsAr.cart_start_order}
        </a>
      </div>
    `;
    cartSummaryFooter.style.display = "none";
    return;
  }
  
  cartSummaryFooter.style.display = "flex";
  list.innerHTML = "";
  
  let subtotal = 0;
  
  cart.forEach(item => {
    const name = currentLang === "en" ? item.nameEn : item.nameAr;
    const size = currentLang === "en" ? item.sizeNameEn : item.sizeNameAr;
    const spice = currentLang === "en" ? item.spiceNameEn : item.spiceNameAr;
    
    // Construct addon labels
    const addonsLabel = item.addons.map(a => currentLang === "en" ? a.labelEn : a.labelAr).join(", ");
    const detailsString = `${size} | ${spice}${addonsLabel ? ` | +${addonsLabel}` : ""}`;
    const removeText = currentLang === "en" ? "Remove" : "حذف";
    
    subtotal += item.unitPrice * item.quantity;
    
    const div = document.createElement("div");
    div.className = "cart-item";
    div.innerHTML = `
      <img src="${item.image}" alt="${name}" class="cart-item-img">
      <div class="cart-item-details">
        <h4 class="cart-item-title">${name}</h4>
        <span class="cart-item-options">${detailsString}</span>
        <div class="cart-item-actions">
          <div class="qty-controls">
            <button class="qty-btn" onclick="updateQty('${item.cartId}', -1)">-</button>
            <span class="qty-num">${item.quantity}</span>
            <button class="qty-btn" onclick="updateQty('${item.cartId}', 1)">+</button>
          </div>
          <span class="cart-item-price">$${(item.unitPrice * item.quantity).toFixed(2)}</span>
          <button class="cart-item-remove" onclick="removeCartItem('${item.cartId}')">${removeText}</button>
        </div>
      </div>
    `;
    list.appendChild(div);
  });
  
  const tax = subtotal * 0.14;
  const total = subtotal + tax;
  
  subtotalVal.textContent = `$${subtotal.toFixed(2)}`;
  taxVal.textContent = `$${tax.toFixed(2)}`;
  grandVal.textContent = `$${total.toFixed(2)}`;
}

window.updateQty = function(cartId, change) {
  const item = cart.find(c => c.cartId === cartId);
  if (!item) return;
  
  item.quantity += change;
  if (item.quantity <= 0) {
    removeCartItem(cartId);
  } else {
    renderCart();
  }
};

window.removeCartItem = function(cartId) {
  cart = cart.filter(c => c.cartId !== cartId);
  renderCart();
};

